import { defineFeature, loadFeature} from "jest-cucumber"
import { shallow, ShallowWrapper } from 'enzyme'

import * as helpers from '../../../../framework/src/Helpers'
import {runEngine} from '../../../../framework/src/RunEngine'
import {Message} from "../../../../framework/src/Message"

import MessageEnum, {getName} from "../../../../framework/src/Messages/MessageEnum"; 
import React from "react";
import Translation from "../../src/Translation"
const navigation = require("react-navigation")

const screenProps = {
    navigation: navigation,
    id: "Translation"
  }

const feature = loadFeature('./__tests__/features/Translation-scenario.feature');

defineFeature(feature, (test) => {


    beforeEach(() => {
        jest.resetModules();
        jest.doMock('react-native', () => ({ Platform: { OS: 'web' }}));
        jest.spyOn(helpers, 'getOS').mockImplementation(() => 'web');
    });

    test('User navigates to Translation', ({ given, when, then }) => {
        let exampleBlockA:ShallowWrapper;
        let instance:Translation; 

        given('I am a User loading Translation', () => {
            exampleBlockA = shallow(<Translation {...screenProps}/>);
        });

        when('I navigate to the Translation', () => {
             instance = exampleBlockA.instance() as Translation
        });

        then('Translation will load with out errors', () => {
            expect(exampleBlockA).toBeTruthy();
        });

        then('I can enter text with out errors', () => {
            let textInputComponent = exampleBlockA.findWhere((node) => node.prop('testID') === 'txtInput');
            textInputComponent.simulate('changeText', 'hello');
        });

        then('I can select the button with with out errors', () => {
            let buttonComponent = exampleBlockA.findWhere((node) => node.prop('testID') === 'btnTranslate');
            buttonComponent.simulate('press');
        });

        then('I can leave the screen with out errors', () => {
            instance.componentWillUnmount()
            expect(exampleBlockA).toBeTruthy();
        });
    });


});
